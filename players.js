export function getJersey(player){
    return player.split("|")[0];
}

export function getName(player){
    return player.split("|")[1];
}

export function getLastName(player){
    let playerName = player.split("|")[1].split(" ");
    let lastName = "";
    for(let i=1; i<playerName.length; i++){
        lastName += playerName[i];
        if(playerName.length - i > 1)
            lastName += " ";
    }
    return lastName;
}

export function getConference(player){
    return player.split("|")[2];
}

